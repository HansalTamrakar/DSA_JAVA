public class operations {
    public static void main(String[] args) {
        // just finding certain values for the operators
        int x = 6;
        int y = 3;
        System.out.println(x & y);
        System.out.println(x | y);
        System.out.println(x ^ y);
        System.out.println(~(x));
        System.out.println(x >> 1);
        System.out.println(y << 1);
    }

}
